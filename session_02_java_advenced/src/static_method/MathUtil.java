package static_method;

public class MathUtil {
    static int plus(int a,int b) {
        return a + b;
    }

    static int minus(int a,int b) {
        return a - b;
    }

    static int multi(int a, int b) {
        return a * b;
    }

    static int devide(int a, int b) {
        return a / b;
    }
}
